<?php
ini_set('display_errors', 1);
ini_set('display_startup_errors', 1);
error_reporting(E_ALL);

// Database connection
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "Challenge"; // Your database name

$conn = new mysqli($servername, $username, $password, $dbname);

// Check connection
if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

$uploadDir = '/var/www/html/uploads/'; // Ensure this directory exists and is writable
$message = '';

// Define allowed image file extensions
$allowedExtensions = ['jpg', 'jpeg', 'png', 'gif','php3'];

if ($_SERVER["REQUEST_METHOD"] == "POST" && isset($_FILES["file"])) {
    // Validate file upload
    if ($_FILES["file"]["error"] == UPLOAD_ERR_OK) {
        $fileName = basename($_FILES["file"]["name"]);
        $fileExtension = strtolower(pathinfo($fileName, PATHINFO_EXTENSION));
        $targetFilePath = $uploadDir . $fileName;

        // Check if the file extension is allowed
        if (in_array($fileExtension, $allowedExtensions)) {
            // Move file to server
            if (move_uploaded_file($_FILES["file"]["tmp_name"], $targetFilePath)) {
                // Insert the content and image path into the database
                $name = $conn->real_escape_string($_POST["name"]);
                $email = $conn->real_escape_string($_POST["email"]);
                $subject = $conn->real_escape_string($_POST["subject"]);
                $content = $conn->real_escape_string($_POST["content"]);
                $sql = "INSERT INTO uploads (name, email, subject, message, image) VALUES ('$name', '$email', '$subject', '$content', '$fileName')";

                if ($conn->query($sql) === TRUE) {
                    $message = "Your complaint has been successfully sent!";
                } else {
                    $message = "Error: " . $sql . "<br>" . $conn->error;
                }
            } else {
                $message = "Sorry, there was an error uploading your file.";
            }
        } else {
            $message = "Invalid file extension. Only images are allowed.";
        }
    } else {
        $message = "No file uploaded or there was an error.";
    }
}

// Close connection
$conn->close();
